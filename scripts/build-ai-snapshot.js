// Build-time snapshot of the source files ai-service/src/analyzer.js scans
// for code evidence. In local dev, analyzer.js reads these files live off
// disk (REPO_ROOT relative reads across sibling packages). In the deployed
// Vercel bundle, sibling-package files aren't traced/bundled by default
// since the paths are computed at runtime from data in knowledge.js, not
// string literals — so we snapshot them here and analyzer.js falls back to
// this module when the live disk read fails (see evidenceFor()).
//
// Self-maintaining: pulls the file list straight from knowledge.js, so new
// entities/impacts are picked up automatically on the next build.
const fs = require('fs');
const path = require('path');

const REPO_ROOT = path.join(__dirname, '..');
const { ENTITIES } = require(path.join(REPO_ROOT, 'ai-service/src/knowledge'));

const files = new Set();
for (const entity of Object.values(ENTITIES)) {
  for (const impact of entity.impacts) files.add(impact.file);
}

const snapshot = {};
for (const file of files) {
  snapshot[file] = fs.readFileSync(path.join(REPO_ROOT, file), 'utf8');
}

const outPath = path.join(REPO_ROOT, 'ai-service/src/fileSnapshot.generated.js');
const header = '// AUTO-GENERATED by scripts/build-ai-snapshot.js — do not edit by hand.\n';
fs.writeFileSync(outPath, `${header}module.exports = ${JSON.stringify(snapshot, null, 2)};\n`);

console.log(`[build-ai-snapshot] wrote ${files.size} file(s) to ${path.relative(REPO_ROOT, outPath)}`);
